public class Integration {
	
	/**
	 * Generate a katalon custom keyword to connect to SQL Server using JDBC 
	 */
@Keyword
def Connection connectToSqlServer(String serverName, String databaseName, String username, String password) {
    String connectionString = "jdbc:sqlserver://" + serverName + ";databaseName=" + databaseName + ";user=" + username + ";password=" + password
    Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver")
    Connection connection = DriverManager.getConnection(connectionString)
    return connection
}
	
